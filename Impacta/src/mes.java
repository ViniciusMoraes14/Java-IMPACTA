import java.util.*;

public class mes {
	
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		int M;
		
		System.out.println("Digite o Mês: ");
		M = sc.nextInt();
		
		if(M == 1) {System.out.println("Janeiro");};
		if(M == 2) {System.out.println("Fevereiro");};
		if(M == 3) {System.out.println("Março");};
		if(M == 4) {System.out.println("Abril");};
		if(M == 5) {System.out.println("Maio");};
		if(M == 6) {System.out.println("Junho");};
		if(M == 7) {System.out.println("Julho");};
		if(M == 8) {System.out.println("Agosto");};
		if(M == 9) {System.out.println("Setembro");};
		if(M == 10) {System.out.println("Outubro");};
		if(M == 11) {System.out.println("Novembro");};
		if(M == 12) {System.out.println("Dezembro");};
		
		
	}

}

